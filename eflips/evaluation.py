        for j in range(Z.shape[1]):
            Z[0][j] = _get_last_soc(soc_primary, t[0] + j * (t[-1] - t[0]) / Z.shape[1])
        Z[1] = Z[0]
        axin.contourf(Z, Z.shape[1], cmap=cm.RdYlGn, vmin=0.0, vmax=1.0)

